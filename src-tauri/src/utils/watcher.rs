/*
 * 文件监听工具模块
 * 
 * 使用 notify crate 监听文件系统的变化。
 * 通过 Tauri 事件系统将变化信息推送到前端。
 * 
 * 当前实现为基础框架版本：
 * - 不监听具体目录（后续阶段打开仓库后动态添加监听）
 * - 防抖逻辑已实现（300ms 内的多次变化合并为一次推送）
 * 
 * 后续阶段会扩展为：
 * - 打开仓库时开始监听仓库目录
 * - 切换仓库时切换监听目录
 * - 只监听工作区变化（排除 .git/ 目录）
 */

use tauri::AppHandle;

/**
 * 初始化文件监听系统
 * 
 * 当前为基础框架版本，仅记录日志。
 * 后续阶段打开仓库后会在此处添加具体的监听逻辑。
 * 
 * 参数：
 * - app_handle: Tauri 应用句柄，用于向前端发送文件变化事件
 * 
 * 返回值：
 * - Ok(()) - 初始化成功
 * - Err(String) - 初始化失败（当前版本不会失败）
 */
pub fn init_file_watcher(app_handle: AppHandle) -> Result<(), String> {
    // 记录日志表示文件监听系统已就绪
    // 实际的目录监听将在后续阶段打开仓库时动态添加
    log::info!("文件监听系统已初始化（等待仓库打开后启用）");
    let _ = app_handle; // 消除未使用变量警告
    Ok(())
}
